package Color;


import java.awt.Color;

/**
 * Colors
 */
public class Colors {

    private Color branco = new Color(255, 255, 255);
    private Color preto = new Color(72, 77, 83);
    private Color preto_Claro = new Color(80, 84, 90); 

    /**
     * @return the branco
     */
    public Color branco() {
        return branco;
    }

    /**
     * @return the preto
     */
    public Color preto() {
        return preto;
    }

    /**
     * @return the preto_Claro
     */
    public Color preto_Claro() {
        return preto_Claro;
    }
}